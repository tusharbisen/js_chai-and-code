// Continue in Control Flow  Switch Case 

const march = 4;

switch (march) {
    case 1:
        console.log("Januray");
        
        break;
    case 2:
        console.log("Februwary");
        
        break;
    case 3:
        console.log("March");
        
        break;
    case 4:
        console.log("April");
        
        break;

    default:
        console.log("This is Default Case");
        
        break;
}